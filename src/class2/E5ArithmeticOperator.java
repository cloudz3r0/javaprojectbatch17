package class2;

public class E5ArithmeticOperator {
    public static void main(String[] args) {
        int applePrice=5;
        int orangePrice=4;
        int totalPrice=applePrice+orangePrice;
        System.out.println(totalPrice);

//plus sign performs addition if in int or byte or long. With string it just adds them together
        int price1=10;
        int price2=20;
        int wholePrice=price1*price2;
        System.out.println(wholePrice);
// can do either one of these two
        System.out.println(price1*price2);

    }
}
