package class7;

public class E9Array {
    public static void main(String[] args) {
        String []fruit={"Apple","Orange","Banana","Kiwi","Pineapple"};
        System.out.println(fruit.length);//gives us number of items in array.
        for (int i = 0; i < fruit.length; i++) {
            System.out.println(fruit[i]);//loops all the array values
        }
    }
}
