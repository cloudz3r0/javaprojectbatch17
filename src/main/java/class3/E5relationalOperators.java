package class3;

public class E5relationalOperators {
    public static void main(String[] args) {
        System.out.println(10>15);//will say false like last tab.
        System.out.println(10<15);//will say its true. 10 is less than 15
        System.out.println(10==15);// is 10 = to 15 (==) means logical equal if you use = it will asign value on the left
        System.out.println(10!=15);// will say true 10 is not equal to 15
        System.out.println(10>=15);//asking if 10 is greater than or equal to 15
        System.out.println(10<=15);//is 10 less than or equal to 15.
    }
}
