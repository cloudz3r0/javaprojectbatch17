package class7;

public class T6NestedLoop {
    public static void main(String[] args) {
        /*
        ######
        ######
        ######
        ######
         */
        for (int i = 1; i <=4; i++) {//creates 4 lines
            for (int j = 1; j <=6 ; j++) {//makes 6 # symbols
                System.out.print('#');
            }
            System.out.println();
        }
    }
}
