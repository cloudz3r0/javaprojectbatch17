package class2;

public class VariableValues {
    public static void main(String[] args) {
        byte numberPens=2;
        System.out.println(numberPens);
        numberPens=4;
        System.out.println(numberPens);
        int bigNumber=35425;
        System.out.println(bigNumber);
        bigNumber=36000;
        System.out.println(bigNumber);
        long biggerNumber=35772552451L;
        System.out.println(biggerNumber);
        biggerNumber=5463642512535l;
        System.out.println(biggerNumber);
        short wholeNumber=6563;
        System.out.println(wholeNumber);
        wholeNumber=6442;
        System.out.println(wholeNumber);
        float smallDecimal= 5.72F;
        System.out.println(smallDecimal);
        smallDecimal=7.52F;
        System.out.println(smallDecimal);
        char hi='A';
        System.out.println(hi);
        hi='G';
        System.out.println(hi);
        boolean wife=true;
        System.out.println(wife);
        wife=false;
        System.out.println(wife);
    }
}//only do all this if needed to print the previous and new values
