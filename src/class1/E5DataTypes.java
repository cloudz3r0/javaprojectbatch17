package class1;

public class E5DataTypes {

    public static void main(String[] args) {
        //float is for decimal numbers. F at the end of the number. Won't store after 6th digit.
        //double is for large decimal numbers
        //double won't store the numbers after the 14th or 15th digit
        float price=12.2367545F;
        double pi=3.145264758696066356;
        System.out.println(price);
        System.out.println(pi);


    }
}
