package class6;

public class E11DoWhileLoop {
    public static void main(String[] args) {
        int i=5;

        do{//do will come before the while. At the end it checks the condition. So if
            //value output is false it will print the value then check while condition.
            // then becomes false
            System.out.println(i);
          i+=5;
        }while (i<=30);






    }
}
